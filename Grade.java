public class Grade {
    public static void main(String[] args){
        int marks=9;
        if(marks>90){
            System.out.println("Excellent");
        } 
        else if(marks>80){
            System.out.println("Great");
        }
        else if(marks>60){
            System.out.println("Good");
        }
        else{
            System.out.println("Better Luck Next Time");
        }
    }
}
